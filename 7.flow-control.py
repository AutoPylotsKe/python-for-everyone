#if statements assists us to make decisions around boolean output
day_of_week = input("What day of the week are you in? ")

if day_of_week == "Monday":
    print("have a blessed start of the week")
elif day_of_week == "Tuesday":
    print("have a blessed start of the week")
else:
    print("have a blessed day of the week")

# if day of the week is Monday/Tuesday, it prints "have a blessed start of the week", if not Monday/Tuesday it proceeds to print "have a blessed day of the week"